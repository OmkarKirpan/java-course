/**
 * Loops
 *
 * @author t011047
 */
public class Loops {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int i = 1;
		while(i <= 10)
		{
			System.out.println(i++);
		}
		System.out.println();
		i = 1;
		do {
			System.out.println(i++);
		} while(i <= 10);
	}

}
