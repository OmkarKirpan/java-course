package net.azib.java.students.t040750.homework.generic;

/**
 * The {@link Result} class represents the collection of results for every
 * individual event in decathlon.
 * 
 * @author Karpz
 *
 */
public class Result {
	
	float run100m;
	float longJump;
	float shotPut;
	float highJump;
	float run400m;
	float hurdles110m;
	float discus;
	float poleVault;
	float javelin;
	float run1500m;
	
	public Result() { }
	
	public float getRun100m() {
		return run100m;
	}

	public void setRun100m(float run100m) {
		this.run100m = run100m;
	}

	public float getLongJump() {
		return longJump;
	}

	public void setLongJump(float longJump) {
		this.longJump = longJump;
	}

	public float getShotPut() {
		return shotPut;
	}

	public void setShotPut(float shotPut) {
		this.shotPut = shotPut;
	}

	public float getHighJump() {
		return highJump;
	}

	public void setHighJump(float highJump) {
		this.highJump = highJump;
	}

	public float getRun400m() {
		return run400m;
	}

	public void setRun400m(float run400m) {
		this.run400m = run400m;
	}

	public float getHurdles110m() {
		return hurdles110m;
	}

	public void setHurdles110m(float hurdles110m) {
		this.hurdles110m = hurdles110m;
	}

	public float getDiscus() {
		return discus;
	}

	public void setDiscus(float discus) {
		this.discus = discus;
	}

	public float getPoleVault() {
		return poleVault;
	}

	public void setPoleVault(float poleVault) {
		this.poleVault = poleVault;
	}

	public float getJavelin() {
		return javelin;
	}

	public void setJavelin(float javelin) {
		this.javelin = javelin;
	}

	public float getRun1500m() {
		return run1500m;
	}

	public void setRun1500m(float run1500m) {
		this.run1500m = run1500m;
	}
}
