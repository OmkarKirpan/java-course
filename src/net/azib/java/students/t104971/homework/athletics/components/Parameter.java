/**
 * @author Jaroslav Judin
 */
public class Parameter {


    private double a;
    private double b;
    private double c;

    /**
     * @param a
     * @param b
     * @param c
     */
    public Parameter(double a, double b, double c) {
        super();
        this.a = a;
        this.b = b;
        this.c = c;
    }

    /**
     * @return the a
     */
    public double getA() {
        return a;
    }

    /**
     * @return the b
     */
    public double getB() {
        return b;
    }

    /**
     * @return the c
     */
    public double getC() {
        return c;
    }


}