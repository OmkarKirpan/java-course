/**
 * Calculator
 *
 * @author t073639
 */
public class Calculator {

	public double multiply(double i, double j) {
		return i*j;
	}

	public double divide(double d1, double d2) {
		return d1/d2;
	}

	public double add(double d1, double d2) {
		return d1+d2;
	}

	public double subtract(double d1, double d2) {
		return d1-d2;
	}

}
